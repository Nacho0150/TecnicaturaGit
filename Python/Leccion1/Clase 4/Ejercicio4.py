# Ejercicio Clase 4: Determinar si es mayor de edad
#
# Pedir un número al usuario
# Almacenar el valor en una variable
# Usar la estructura ‘if else’
# La formula es: <num> >= 18
# True: Eres mayor de edad, Imprimir
# False: Eres menor de edad, Imprimir
# Vamos a hacer este ejercicio en PyCharm

num = int(input("Ingrese su edad y le diremos si es mayor o no "))

if (num >= 18):
    print("Eres mayor de edad")
else:
    print("Eres menor de edad")